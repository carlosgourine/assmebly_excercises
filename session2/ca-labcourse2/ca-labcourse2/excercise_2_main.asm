	#ifdef RAM0	
	; clear RAM0
ram0_start.b EQU $ram0_segment_start
ram0_end.b EQU $ram0_segment_end
	ldw X,#ram0_start
clear_ram0.l
	clr (X)
	incw X
	cpw X,#ram0_end	
	jrule clear_ram0
	#endif

	#ifdef RAM1
	; clear RAM1
ram1_start.w EQU $ram1_segment_start
ram1_end.w EQU $ram1_segment_end	
	ldw X,#ram1_start
clear_ram1.l
	clr (X)
	incw X
	cpw X,#ram1_end	
	jrule clear_ram1
	#endif

	; clear stack
stack_start.w EQU $stack_segment_start
stack_end.w EQU $stack_segment_end
	ldw X,#stack_start
clear_stack.l
	clr (X)
	incw X
	cpw X,#stack_end	
	jrule clear_stack
	bset   PD_DDR,#4
	bset   PD_CR1,#4
	bres   PD_ODR,#4
	bres   PA_DDR,#3   ; PA3 input
  bset   PA_CR1,#3   ; pull-up
  bres   PE_DDR,#5   ; PE5 input
  bset   PE_CR1,#5   ; pull-up
	clr    TIM2_CR1
	clr    TIM2_PSCR
	ldw    X,#399
	ld     A,XH
	mov     TIM2_ARRH,A
	ld     A,XL
	mov     TIM2_ARRL,A
	ldw    X,#100
	ld     A,XH
	ld     TIM2_CRRH,A
	ld     A,XL
	ld     TIM2_CRRL,A
	mov     TIM2_CCMR1,#%01100000
	bset    TIM2_CCER1,#0 
	bset   TIM2_CR1,#7
	bset   TIM2_EGR,#0
	bset   TIM2_CR1,#0 
infinite_loop1.l
  btjf    PA_IDR,#3,no_start
	bset    TIM2_CR1,#0
	jra infinite_loop1 ;jump to label infinite_loop1
no_start
  btjf    PE_IDR,#5,no_stop
	bres    TIM2_CR1,#0
no_stop
  
	interrupt NonHandledInterrupt
NonHandledInterrupt.l
	iret

	segment 'vectit'
	dc.l {$82000000+main}									; reset
